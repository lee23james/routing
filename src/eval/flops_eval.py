"""FLOPs-based evaluation following EcoTab methodology.

Metrics:
  - Acc@60% LRM-FLOPs: accuracy when budget = 60% of LRM-only FLOPs
  - FLOPs@98% LRM-Acc: FLOPs needed to reach 98% of LRM-only accuracy
  - A/F: Accuracy per FLOPs (higher is better)
  - Full accuracy-FLOPs trade-off curves

FLOPs formula: 2 * N_params * n_tokens (standard transformer approximation)
  SRM (Qwen3-1.7B): 3.4e9 FLOPs per token
  LRM (Qwen3-14B):  28.0e9 FLOPs per token

Usage:
    python -m eval.flops_eval --episodes_path ../data/episodes/all_episodes.jsonl
"""

import argparse
import json
import os
import sys
from typing import Dict, List, Optional, Tuple

import numpy as np
import torch

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from config import (
    STATE_DIM, HIDDEN_DIM, ACTION_DIM,
    CHECKPOINTS_DIR, RESULTS_DIR,
)
from router.policy import RouterPolicy
from router.env import TRIMEnv
from data.datasets import load_jsonl

SRM_PARAMS = 1.7e9
LRM_PARAMS = 14.0e9
SRM_FLOPS_PER_TOKEN = 2 * SRM_PARAMS
LRM_FLOPS_PER_TOKEN = 2 * LRM_PARAMS


def compute_episode_flops(ep: Dict, actions: List[int]) -> float:
    """Compute FLOPs for a routing trajectory.

    For each step:
    - action=0 (SRM): srm_token_count * SRM_FLOPS
    - action=1 (LRM): lrm_token_count * LRM_FLOPS + srm_token_count * SRM_FLOPS
      (SRM generates first for PRM scoring, then LRM regenerates)
    """
    srm_counts = ep.get("srm_token_counts", [])
    lrm_counts = ep.get("lrm_token_counts", [])
    n = min(len(actions), len(srm_counts))
    total_flops = 0.0
    for i in range(n):
        srm_tok = srm_counts[i] if i < len(srm_counts) else 10
        lrm_tok = lrm_counts[i] if i < len(lrm_counts) else 10
        total_flops += srm_tok * SRM_FLOPS_PER_TOKEN
        if actions[i] == 1:
            total_flops += lrm_tok * LRM_FLOPS_PER_TOKEN
    return total_flops


def compute_lrm_only_flops(ep: Dict) -> float:
    """LRM-only baseline: all tokens generated by LRM."""
    lrm_total = ep.get("lrm_total_tokens", 0)
    if lrm_total == 0:
        lrm_counts = ep.get("lrm_token_counts", [])
        lrm_total = sum(lrm_counts)
    return lrm_total * LRM_FLOPS_PER_TOKEN


def compute_srm_only_flops(ep: Dict) -> float:
    srm_total = ep.get("srm_total_tokens", 0)
    if srm_total == 0:
        srm_counts = ep.get("srm_token_counts", [])
        srm_total = sum(srm_counts)
    return srm_total * SRM_FLOPS_PER_TOKEN


def estimate_mixed_correctness(ep: Dict, actions: List[int]) -> bool:
    """Estimate correctness for a mixed routing trajectory.

    Uses PRM trajectory quality to estimate whether the mixed solution
    is closer to SRM-quality or LRM-quality, avoiding direct label leakage.
    """
    n_regens = sum(actions)
    n_steps = len(actions)

    if n_regens == 0:
        return ep.get("srm_correct", False)
    if n_regens == n_steps:
        return ep.get("lrm_correct", False)

    srm_correct = ep.get("srm_correct", False)
    lrm_correct = ep.get("lrm_correct", False)
    if srm_correct == lrm_correct:
        return srm_correct

    srm_prm = ep.get("srm_prm_scores", [])
    lrm_prm = ep.get("lrm_prm_scores", [])
    n = min(len(actions), len(srm_prm))

    chosen_prm = []
    for i in range(n):
        if actions[i] == 1 and i < len(lrm_prm):
            chosen_prm.append(lrm_prm[i])
        elif i < len(srm_prm):
            chosen_prm.append(srm_prm[i])

    if not chosen_prm:
        return lrm_correct if n_regens >= n_steps / 2 else srm_correct

    mean_prm = sum(chosen_prm) / len(chosen_prm)
    srm_mean = sum(srm_prm) / len(srm_prm) if srm_prm else 0.5
    lrm_mean = sum(lrm_prm) / len(lrm_prm) if lrm_prm else 0.5

    if lrm_mean <= srm_mean:
        return lrm_correct if n_regens >= n_steps / 2 else srm_correct

    progress = (mean_prm - srm_mean) / max(lrm_mean - srm_mean, 1e-8)
    return lrm_correct if progress >= 0.4 else srm_correct


# ============================================================
# Threshold-based routing (for accuracy-FLOPs curves)
# ============================================================

def threshold_route_episode(ep: Dict, threshold: float,
                            use_rubric: bool = False,
                            rubric_weights: Optional[Dict] = None) -> Tuple[List[int], bool, float]:
    """Route an episode using a simple PRM threshold.

    action=1 (escalate) if SRM PRM score < threshold.
    Optionally adjust threshold using rubric context.
    """
    srm_prm = ep.get("srm_prm_scores", [])
    lrm_prm = ep.get("lrm_prm_scores", [])
    n = len(srm_prm)
    actions = []

    for i in range(n):
        effective_threshold = threshold
        if use_rubric and i > 0:
            if i >= 2 and srm_prm[i] < srm_prm[i-1] < srm_prm[i-2]:
                effective_threshold += 0.1
            if i >= 1 and srm_prm[i] < srm_prm[i-1] - 0.15:
                effective_threshold += 0.05

        actions.append(1 if srm_prm[i] < effective_threshold else 0)

    correct = estimate_mixed_correctness(ep, actions)
    flops = compute_episode_flops(ep, actions)
    return actions, correct, flops


def sweep_thresholds(episodes: List[Dict],
                     use_rubric: bool = False,
                     rubric_weights: Optional[Dict] = None,
                     thresholds: Optional[List[float]] = None) -> List[Dict]:
    """Sweep PRM thresholds to produce accuracy-FLOPs curves."""
    if thresholds is None:
        thresholds = [i * 0.05 for i in range(21)]

    results = []
    for thresh in thresholds:
        correct = 0
        total_flops = 0.0
        total_regens = 0
        total_steps = 0

        for ep in episodes:
            actions, is_correct, flops = threshold_route_episode(
                ep, thresh, use_rubric, rubric_weights
            )
            correct += int(is_correct)
            total_flops += flops
            total_regens += sum(actions)
            total_steps += len(actions)

        n = len(episodes)
        results.append({
            "threshold": thresh,
            "accuracy": correct / n,
            "avg_flops": total_flops / n,
            "total_flops": total_flops,
            "regen_ratio": total_regens / max(total_steps, 1),
            "correct": correct,
        })

    return results


# ============================================================
# PPO policy routing evaluation with FLOPs
# ============================================================

def evaluate_ppo_policy(episodes_path: str, checkpoint: str,
                        device: str = "cpu") -> Dict:
    """Evaluate a trained PPO policy and compute FLOPs."""
    env = TRIMEnv(episodes_path)
    policy = RouterPolicy(STATE_DIM, HIDDEN_DIM, ACTION_DIM).to(device)
    policy.load_state_dict(torch.load(checkpoint, map_location=device, weights_only=True))
    policy.eval()

    correct = 0
    total_flops = 0.0
    total_regens = 0
    total_steps = 0
    per_problem = []

    for i in range(env.num_episodes):
        state = env.reset(i)
        done = False
        while not done:
            st = torch.FloatTensor(state).unsqueeze(0).to(device)
            with torch.no_grad():
                action, _, _ = policy.get_action(st, deterministic=True)
            state, _, done, _ = env.step(action.item())

        info = env.get_episode_info()
        ep = env.current_ep
        actions = info["actions"]

        is_correct = estimate_mixed_correctness(ep, actions)
        flops = compute_episode_flops(ep, actions)

        correct += int(is_correct)
        total_flops += flops
        total_regens += info["num_regens"]
        total_steps += info["num_steps"]

        per_problem.append({
            "id": info["query_id"],
            "correct": is_correct,
            "actions": actions,
            "flops": flops,
            "num_regens": info["num_regens"],
        })

    n = env.num_episodes
    return {
        "accuracy": correct / n,
        "avg_flops": total_flops / n,
        "regen_ratio": total_regens / max(total_steps, 1),
        "correct": correct,
        "total": n,
        "per_problem": per_problem,
    }


# ============================================================
# Key metrics: Acc@60% and FLOPs@98%
# ============================================================

def find_acc_at_flops_pct(curve: List[Dict], target_flops: float) -> float:
    """Find accuracy at a target FLOPs budget via linear interpolation."""
    sorted_curve = sorted(curve, key=lambda x: x["avg_flops"])
    for i, point in enumerate(sorted_curve):
        if point["avg_flops"] >= target_flops:
            if i == 0:
                return point["accuracy"]
            prev = sorted_curve[i - 1]
            frac = (target_flops - prev["avg_flops"]) / max(point["avg_flops"] - prev["avg_flops"], 1e-20)
            return prev["accuracy"] + frac * (point["accuracy"] - prev["accuracy"])
    return sorted_curve[-1]["accuracy"] if sorted_curve else 0.0


def find_flops_at_acc_pct(curve: List[Dict], target_acc: float) -> float:
    """Find minimum FLOPs to achieve target accuracy via interpolation."""
    sorted_curve = sorted(curve, key=lambda x: x["avg_flops"])
    for i, point in enumerate(sorted_curve):
        if point["accuracy"] >= target_acc:
            return point["avg_flops"]
    return sorted_curve[-1]["avg_flops"] if sorted_curve else float("inf")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--episodes_path", type=str, required=True)
    parser.add_argument("--output_dir", type=str, default=None)
    parser.add_argument("--rubric_weights", type=str, default=None)
    args = parser.parse_args()

    episodes = load_jsonl(args.episodes_path)
    print(f"Loaded {len(episodes)} episodes")

    if args.output_dir is None:
        args.output_dir = os.path.join(RESULTS_DIR, "flops_evaluation")
    os.makedirs(args.output_dir, exist_ok=True)

    rubric_w = None
    if args.rubric_weights and os.path.exists(args.rubric_weights):
        with open(args.rubric_weights) as f:
            rubric_w = json.load(f).get("weights", None)

    n = len(episodes)

    # ---- Baselines ----
    srm_correct = sum(1 for ep in episodes if ep.get("srm_correct", False))
    lrm_correct = sum(1 for ep in episodes if ep.get("lrm_correct", False))
    srm_acc = srm_correct / n
    lrm_acc = lrm_correct / n

    srm_flops = np.mean([compute_srm_only_flops(ep) for ep in episodes])
    lrm_flops = np.mean([compute_lrm_only_flops(ep) for ep in episodes])

    print(f"\nBaselines:")
    print(f"  SRM-only: acc={srm_acc:.4f}  avg_flops={srm_flops:.2e}")
    print(f"  LRM-only: acc={lrm_acc:.4f}  avg_flops={lrm_flops:.2e}")
    print(f"  60% LRM FLOPs budget = {0.6 * lrm_flops:.2e}")
    print(f"  98% LRM accuracy target = {0.98 * lrm_acc:.4f}")

    target_flops_60 = 0.6 * lrm_flops
    target_acc_98 = 0.98 * lrm_acc

    all_results = {
        "SRM_only": {
            "accuracy": srm_acc, "avg_flops": srm_flops,
            "regen_ratio": 0.0, "method": "SRM_only",
        },
        "LRM_only": {
            "accuracy": lrm_acc, "avg_flops": lrm_flops,
            "regen_ratio": 1.0, "method": "LRM_only",
        },
    }

    # ---- Threshold sweep: TRIM-Agg (PRM threshold only) ----
    print("\n=== TRIM-Agg (threshold sweep) ===")
    trim_agg_curve = sweep_thresholds(episodes, use_rubric=False)
    for pt in trim_agg_curve:
        print(f"  τ={pt['threshold']:.2f}  acc={pt['accuracy']:.4f}  "
              f"flops={pt['avg_flops']:.2e}  regen={pt['regen_ratio']:.2%}")

    agg_acc_60 = find_acc_at_flops_pct(trim_agg_curve, target_flops_60)
    agg_flops_98 = find_flops_at_acc_pct(trim_agg_curve, target_acc_98)
    agg_af = agg_acc_60 / (target_flops_60 / 1e12) if target_flops_60 > 0 else 0

    all_results["TRIM-Agg (threshold)"] = {
        "accuracy_at_60pct": round(agg_acc_60, 4),
        "flops_at_98pct": agg_flops_98,
        "A/F": round(agg_af, 4),
        "curve": trim_agg_curve,
    }
    print(f"\n  Acc@60% LRM-FLOPs = {agg_acc_60:.4f}")
    print(f"  FLOPs@98% LRM-Acc = {agg_flops_98:.2e}")

    # ---- Threshold sweep: TRIM-Rubric (PRM threshold + rubric context) ----
    print("\n=== TRIM-Rubric (threshold sweep with rubric context) ===")
    trim_rubric_curve = sweep_thresholds(episodes, use_rubric=True, rubric_weights=rubric_w)
    for pt in trim_rubric_curve:
        print(f"  τ={pt['threshold']:.2f}  acc={pt['accuracy']:.4f}  "
              f"flops={pt['avg_flops']:.2e}  regen={pt['regen_ratio']:.2%}")

    rub_acc_60 = find_acc_at_flops_pct(trim_rubric_curve, target_flops_60)
    rub_flops_98 = find_flops_at_acc_pct(trim_rubric_curve, target_acc_98)
    rub_af = rub_acc_60 / (target_flops_60 / 1e12) if target_flops_60 > 0 else 0

    all_results["TRIM-Rubric (threshold)"] = {
        "accuracy_at_60pct": round(rub_acc_60, 4),
        "flops_at_98pct": rub_flops_98,
        "A/F": round(rub_af, 4),
        "curve": trim_rubric_curve,
    }
    print(f"\n  Acc@60% LRM-FLOPs = {rub_acc_60:.4f}")
    print(f"  FLOPs@98% LRM-Acc = {rub_flops_98:.2e}")

    # ---- PPO-trained policies ----
    print("\n=== PPO-trained policies ===")
    import glob
    for ckpt_dir in sorted(glob.glob(os.path.join(CHECKPOINTS_DIR, "*"))):
        best_path = os.path.join(ckpt_dir, "best.pt")
        if not os.path.exists(best_path):
            continue
        name = os.path.basename(ckpt_dir)
        try:
            result = evaluate_ppo_policy(args.episodes_path, best_path)
            per_p = result.pop("per_problem", [])
            flops_ratio = result["avg_flops"] / lrm_flops if lrm_flops > 0 else 0
            af = result["accuracy"] / (result["avg_flops"] / 1e12) if result["avg_flops"] > 0 else 0
            result["flops_ratio_to_lrm"] = round(flops_ratio, 4)
            result["A/F"] = round(af, 4)
            all_results[f"PPO:{name}"] = result
            print(f"  {name:45s}  acc={result['accuracy']:.4f}  "
                  f"flops={result['avg_flops']:.2e}  "
                  f"({flops_ratio:.1%} of LRM)  "
                  f"regen={result['regen_ratio']:.2%}")
        except Exception as e:
            print(f"  {name}: error — {e}")

    # ---- Summary table ----
    print("\n" + "=" * 120)
    print(f"{'Method':<50} {'Acc':>7} {'AvgFLOPs':>14} {'%LRM':>7} {'Regen%':>8} {'A/F':>8}")
    print("-" * 120)
    for name, r in all_results.items():
        if "curve" in r:
            acc = f"{r.get('accuracy_at_60pct', 0):.4f}"
            flops = f"{r.get('flops_at_98pct', 0):.2e}"
            print(f"{name:<50} {acc:>7} {flops:>14} {'—':>7} {'—':>8} {r.get('A/F', 0):>8.2f}")
        else:
            acc = f"{r.get('accuracy', 0):.4f}"
            flops_val = r.get('avg_flops', 0)
            flops = f"{flops_val:.2e}"
            pct = f"{r.get('flops_ratio_to_lrm', flops_val/lrm_flops if lrm_flops else 0):.1%}"
            regen = f"{r.get('regen_ratio', 0):.2%}"
            af = f"{r.get('A/F', 0):.2f}" if 'A/F' in r else "—"
            print(f"{name:<50} {acc:>7} {flops:>14} {pct:>7} {regen:>8} {af:>8}")
    print("=" * 120)

    # ---- Key results ----
    print(f"\n{'='*60}")
    print(f"  KEY METRICS (EcoTab-style)")
    print(f"{'='*60}")
    print(f"  LRM-only: acc={lrm_acc:.4f}  FLOPs={lrm_flops:.2e}")
    print(f"  60% LRM FLOPs budget = {target_flops_60:.2e}")
    print(f"  98% LRM accuracy target = {target_acc_98:.4f}")
    print()
    print(f"  {'Method':<35} {'Acc@60%FLOPs':>14} {'FLOPs@98%Acc':>14}")
    print(f"  {'-'*65}")
    print(f"  {'TRIM-Agg (threshold)':<35} {agg_acc_60:>14.4f} {agg_flops_98:>14.2e}")
    print(f"  {'TRIM-Rubric (threshold)':<35} {rub_acc_60:>14.4f} {rub_flops_98:>14.2e}")
    print(f"  {'='*60}")

    # Save everything
    save_data = {}
    for name, r in all_results.items():
        save_data[name] = {k: v for k, v in r.items() if k != "per_problem"}
    save_data["meta"] = {
        "srm_params": SRM_PARAMS, "lrm_params": LRM_PARAMS,
        "lrm_only_flops": lrm_flops, "srm_only_flops": srm_flops,
        "target_flops_60pct": target_flops_60,
        "target_acc_98pct": target_acc_98,
    }

    out_path = os.path.join(args.output_dir, "flops_comparison.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(save_data, f, indent=2, ensure_ascii=False, default=str)
    print(f"\nSaved → {out_path}")


if __name__ == "__main__":
    main()
